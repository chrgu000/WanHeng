package com.cgwas.sector.dao.impl;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.stereotype.Service;

import com.cgwas.common.dataaccess.api.AbstractDao;
import com.cgwas.common.dataaccess.api.IDaoSupport;
import com.cgwas.common.dataaccess.api.IPageQuery;
import com.cgwas.common.dataaccess.api.Page;
import com.cgwas.common.dataaccess.api.PageQueryFactory;
import com.cgwas.common.dataaccess.impl.PageQuery;
import com.cgwas.sector.dao.api.ISectorDao;
import com.cgwas.sector.entity.Sector;

@Service
public class SectorDao extends AbstractDao implements ISectorDao {
	private PageQueryFactory<PageQuery> pageQueryFactory;

	public Page page(Page page, Sector sector) {

		IPageQuery pageQuery = pageQueryFactory.getPageQuery(page,
				"selectSectorPage", "selectSectorCount");
		return pageQuery.select(sector);
	}

	@Autowired
	public void setDaoSupport(
			@Qualifier("roofDaoSupport") IDaoSupport daoSupport) {
		this.daoSupport = daoSupport;
	}

	@Autowired
	public void setPageQueryFactory(
			@Qualifier("pageQueryFactory") PageQueryFactory<PageQuery> pageQueryFactory) {
		this.pageQueryFactory = pageQueryFactory;
	}

}